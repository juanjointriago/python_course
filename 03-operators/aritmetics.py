# Arithmetic operators in python
# + addition - substraction * multiplication / division % modulus ** exponentiation // floor division

number1 = 10
number2 = 20
add = number1 + number2
less = number1 - number2
multiply = number1 * number2
division = number1 / number2
modulus = number1 % number2
exponentiation = number1 ** number2
floorDivision = number1 // number2
print(f"Addition: {add}")
print(f"Substraction: {less}")
print(f"Multiplication: {multiply}")
print(f"Division: {division}")
print(f"Modulus: {modulus}")
print(f"Exponentiation: {exponentiation}")
print(f"Floor Division: {floorDivision}")